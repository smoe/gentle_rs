fn image_size_inches(image: &LabAssistantEmbeddedImage) -> (f64, f64) {
    let max_width = 6.5f64;
    let width = image.width_px.max(1) as f64;
    let height = image.height_px.max(1) as f64;
    let rendered_height = (max_width * height / width).min(4.5);
    (max_width, rendered_height)
}

fn image_size_emu(image: &LabAssistantEmbeddedImage) -> (u64, u64) {
    let (width_in, height_in) = image_size_inches(image);
    (
        (width_in * 914_400.0).round() as u64,
        (height_in * 914_400.0).round() as u64,
    )
}
